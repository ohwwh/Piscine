int	ft_iterative_power(int nb, int power)
{
	int	ret;
	int	i;

	ret = 1;
	i = 1;
	if (power < 0)
		return (0);
	while (i <= power)
	{
		ret *= nb;
		i ++;
	}
	return (ret);
}
