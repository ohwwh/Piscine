int	ft_sqrt(int nb)
{
	int	temp;

	temp = 1;
	while (temp * temp <= nb)
	{
		if (temp * temp == nb)
			return (temp);
		temp ++;
		if (temp > 46340)
			return (0);
	}
	return (0);
}
