#include <unistd.h>

void	recur(int i, char c, char a[])
{
	if (i <= 3)
	{
		while (c <= '9')
		{
			a[i - 1] = c;
			recur(i + 1, c + 1, a);
			c += 1;
		}
	}	
	else
	{
		if (a[2] != 2 + '0')
			write(1, ", ", 2);
		write(1, a, 3);
	}
}

void	ft_print_comb(void)
{
	char	ar[3];
	char	a;

	a = '0';
	recur(1, a, ar);
}
