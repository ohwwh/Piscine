#include <unistd.h>

void	pos(int n)
{
	char	ar[11];
	int		i;
	char	c;

	i = 0;
	if (i == 0)
		write(1, "0", 1);
	while (n != 0)
	{
		i++;
		c = (n % 10) + '0';
		ar[i] = c;
		n /= 10;
	}
	while (i != 0)
	{	
		write(1, &ar[i], 1);
		i --;
	}
}

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else if (n < 0)
	{
		write(1, "-", 1);
		pos(-n);
	}
	else
		pos(n);
}
