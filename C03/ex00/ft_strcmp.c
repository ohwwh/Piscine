#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{	
	while ((*s1 != '\0' | *s2 != '\0') && *s2 == *s1)
	{
		s1 ++;
		s2 ++;
	}
	return (*s1 - *s2);
}

int	main(void)
{
	char str1[6] = "hello";
	char str2[6] = "hello";

	printf("%d", ft_strcmp(str1, str2));
}
